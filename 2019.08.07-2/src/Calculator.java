public class Calculator {
    public static int add(int a, int b) {
        return a + b;
    }
    public static int diff(int a, int b) {
        return a - b;
    }
    public static int multi(int a, int b) {
        return a * b;
    }

    public static int div(int a, int b) {
        return /*b==0?0:*/a / b;
    }




}
