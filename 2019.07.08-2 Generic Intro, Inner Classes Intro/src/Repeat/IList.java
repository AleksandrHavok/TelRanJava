package Repeat;

//Введение в дженерики
//Дженерики используются как универсальные классы
//вместо T подставляется необходимый нам тип, это может быть ссылочный тип.
// Использовать примитивные типы вроде int нельзя
//В нашем случае мы хотим сделать интерфейс, который сможет обрабатывать массив типа T
public interface IList <T> {
    boolean add(T value);
    boolean remove(int index);
    int indexOf(T value);
    T get(int index);
    int size();
}
